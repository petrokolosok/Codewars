function switchItUp(number){
    switch(number) {
        case 0: return "Zero";
        case number = 1: return "One";
        case 2: return "Two";
        case number = 3: return "Three";
        case 4: return "Four";
        case number = 5: return "Five";
        case 6: return "Six";
        case number = 7: return "Seven";
        case 8: return "Eight";
        case number = 9: return "Nine"
    }
}